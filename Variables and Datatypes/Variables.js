/*

1. Variable name must be meaningful
2. Javascript is dynamically typed programming language 
   (No need of specifying the data type while declaring only) 
3. Variable names are case-sensitive.
4. Reserved words cannot be variable names

*/

fullName = "Sonali";   
age = 20;
price = 99.99;
x = null;
y = undefined;
isFollow = true;
console.log(fullName);   // Sonali
console.log(age);        // 20
console.log(price);      // 99.99
console.log(x);          // null
console.log(y);          // undefined
console.log(isFollow);   // true  

